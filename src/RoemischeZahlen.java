
public class RoemischeZahlen {
	
		private int wert;
		private String text;

		public RoemischeZahlen(int wert, String text) {
			this.wert = wert;
			this.text = text;
		}

		public int getWert() {
			return wert;
		}

		public void setWert(int wert) {
			this.wert = wert;
		}

		public String getText() {
			return text;
		}

		public void setText(String text) {
			this.text = text;
		}

	}